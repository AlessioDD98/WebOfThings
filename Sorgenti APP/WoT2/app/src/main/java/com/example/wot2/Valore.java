package com.example.wot2;

public class Valore {
    String nome;
    String val;

    public Valore(String nome, String val) {
        this.nome = nome;
        this.val = val;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getVal() {
        return val;
    }

    public void setVal(String val) {
        this.val = val;
    }
}
